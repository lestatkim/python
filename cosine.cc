#include<iostream>
using namespace std;

int main() {
	double cosine_similarity(double *A, double *B, unsigned int Vector_Length) {
        double dot = 0.0, denom_a = 0.0, denom_b = 0.0;
        for(unsigned int i = 0; i < Vector_Length; ++i) {
            dot += A[i] * B[i] ;
            denom_a += A[i] * A[i];
            denom_b += B[i] * B[i];
        }
    	return dot / (sqrt(denom_a) * sqrt(denom_b));
	}	
}

